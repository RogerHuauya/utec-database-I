CREATE TABLE estudiante(
    dni integer NOT NULL,
    cestudiante varchar(15) NOT NULL,
    carrera varchar(255) NOT NULL
);
CREATE TABLE autor(
    dni integer NOT NULL,
    especialidad varchar(255) NOT NULL
);
CREATE TABLE libro(
    nombre varchar(255) NOT NULL,
    dni integer NOT NULL
);
CREATE TABLE edicion(
    isbn varchar(255) NOT NULL,
     nombre varchar(255) NOT NULL,
     anhio integer NOT NULL,
     idioma varchar (50) NOT NULL
);
CREATE TABLE copia(
    numero integer NOT NULL,
    isbn varchar(255) NOT NULL
);
CREATE TABLE prestamo(
    numero integer NOT NULL,
    dni integer NOT NULL,
    fprestamo date NOT NULL,
    fdevolucion date NOT NULL
);

CREATE TABLE detalleprestamo(
    pnumero integer NOT NULL,
    cnumero integer NOT NULL,
    isbn varchar(255) NOT NULL
);
ALTER TABLE persona ADD CONSTRAINT persona_pk_dni PRIMARY KEY (dni);
